//Number to string conversion

let num1 = 10 + '10';
let num2 = '55' + 44;
let num3 = false + '9';
let num4 = true + 8;

console.log(num1);
console.log(num2);
console.log(num3);
console.log(num4);


//String to number conversion

let num5 = 99 - '59';
let num6 = 99 / '3';
let num7 = 99 * '2';
let num8 = 99 % '3';

console.log(num5);
console.log(num6);
console.log(num7);
console.log(num8);


//Boolean to number conversion

let num9 = true + 1;
let num10 = false + 12;

console.log(num9);
console.log(num10);